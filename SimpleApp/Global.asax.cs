using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.Optimization;
using System.Web.Routing;
using System.Web.Security;
using SimpleApp;
using SimpleApp.BusinessServices;
using SimpleApp.BusinessServices.Contracts;
using SimpleApp.BusinessServices.Impl;

namespace SimpleApp
{
    public class Global : HttpApplication
    {
        void Application_Start(object sender, EventArgs e)
        {
            // Code that runs on application startup
            BundleConfig.RegisterBundles(BundleTable.Bundles);
            AuthConfig.RegisterOpenAuth();
            RouteConfig.RegisterRoutes(RouteTable.Routes);

            // Register service implementations
            ServiceFactory.Instance.RegisterService<IToDoService>(new ToDoService());
            ServiceFactory.Instance.RegisterService<IUserService>(new UserService());
        }

        void Application_End(object sender, EventArgs e)
        {
            //  Code that runs on application shutdown

        }

        void Application_Error(object sender, EventArgs e)
        {
            // Code that runs when an unhandled error occurs

        }
    }
}
